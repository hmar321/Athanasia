CREATE FUNCTION LIMPIAR(str VARCHAR(255))  -- Change nvarchar(MAX) to VARCHAR(255)
RETURNS VARCHAR(255)
BEGIN
  DECLARE clean_str VARCHAR(255);
  SET clean_str = REPLACE(str, 'á', 'a');
  SET clean_str = REPLACE(clean_str, 'é', 'e');
  SET clean_str = REPLACE(clean_str, 'í', 'i');
  SET clean_str = REPLACE(clean_str, 'ó', 'o');
  SET clean_str = REPLACE(clean_str, 'ú', 'u');
  SET clean_str = REPLACE(clean_str, 'Á', 'A');
  SET clean_str = REPLACE(clean_str, 'É', 'E');
  SET clean_str = REPLACE(clean_str, 'Í', 'I');
  SET clean_str = REPLACE(clean_str, 'Ó', 'O');
  SET clean_str = REPLACE(clean_str, 'Ú', 'U');
  SET clean_str = REPLACE(clean_str, '&', '');
  SET clean_str = REPLACE(clean_str, '-', '');
  SET clean_str = REPLACE(clean_str, '_', '');
  SET clean_str = REPLACE(clean_str, '+', '');
  SET clean_str = REPLACE(clean_str, '"', '');
  SET clean_str = REPLACE(clean_str, '''', '');
  SET clean_str = REPLACE(clean_str, ',', '');
  SET clean_str = REPLACE(clean_str, '.', '');
  SET clean_str = REPLACE(clean_str, '?', '');
  SET clean_str = REPLACE(clean_str, '`', '');
  SET clean_str = REPLACE(clean_str, '!', '');
  SET clean_str = REPLACE(clean_str, '¡', '');
  SET clean_str = REPLACE(clean_str, '¿', '');
  SET clean_str = REPLACE(clean_str, 'ñ', 'n');
  SET clean_str = REPLACE(clean_str, 'Ñ', 'N');
  SET clean_str = REPLACE(clean_str, 'ü', 'u');
  SET clean_str = REPLACE(clean_str, 'Ü', 'U');
  SET clean_str = REPLACE(clean_str, ' ', '');
  SET clean_str = UPPER(clean_str);
  RETURN clean_str;
END //

