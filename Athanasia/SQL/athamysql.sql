CREATE PROCEDURE SP_SEARCH_PRODUCTOS(
  @busqueda NVARCHAR(255),
  @posicion INT,
  @ndatos INT,
  @npaginas OUT INT
)
BEGIN
  SET @npaginas = CEILING(COUNT(ID_PRODUCTO) / CAST(@ndatos AS FLOAT));
  SELECT ID_PRODUCTO, ID_LIBRO, TITULO, PORTADA, AUTOR, PRECIO, ID_FORMATO, FORMATO, UNIDADES
  FROM (
    SELECT
      ID_PRODUCTO,
      ID_LIBRO,
      TITULO,
      PORTADA,
      AUTOR,
      PRECIO,
      ID_FORMATO,
      FORMATO,
      UNIDADES,
      ROW_NUMBER() OVER (ORDER BY ID_PRODUCTO) AS POSICION
    FROM (
      SELECT
        DISTINCT ID_PRODUCTO,
        ID_LIBRO,
        TITULO,
        PORTADA,
        AUTOR,
        PRECIO,
        ID_FORMATO,
        FORMATO,
        UNIDADES,
        SAGA,
        ROW_NUMBER() OVER (PARTITION BY TITULO, AUTOR ORDER BY ID_PRODUCTO) AS REPETICION
      FROM V_PRODUCTO_BUSCADO
    ) AS QUERY
    WHERE REPETICION = 1
      AND TITULO IS NOT NULL
      AND AUTOR IS NOT NULL
      AND LIMPIAR(TITULO) LIKE @busqueda
      OR LIMPIAR(AUTOR) LIKE @busqueda
      OR LIMPIAR(SAGA) LIKE @busqueda
  ) AS PAGINATED
  WHERE POSICION >= @ndatos * @posicion - (@ndatos - 1) AND POSICION <= @posicion * @ndatos
  ORDER BY ID_PRODUCTO;
END $$
