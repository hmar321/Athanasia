  ) AS QUERY
  WHERE REPETICION = 1
    AND TITULO IS NOT NULL
    AND AUTOR IS NOT NULL
    AND LIMPIAR(TITULO) LIKE @busqueda
    OR LIMPIAR(AUTOR) LIKE @busqueda
    OR LIMPIAR(SAGA) LIKE @busqueda
  ORDER BY ID_PRODUCTO;
END $$
